dia = int(input('Insira um número correspondente a um dia da semana (ex: 1 - domingo, 2 - segunda, etc...): '))

if dia == 1:
    print('Hoje é domingo!')
elif dia == 2:
    print('Hoje é segunda!')
elif dia == 3:
    print('Hoje é terça!')
elif dia == 4:
    print('Hoje é quarta!')
elif dia == 5:
    print('Hoje é quinta!')
elif dia == 6:
    print('Hoje é sexta!')
elif dia == 7:
    print('Hoje é sábado!')
else:
    print('Valor inválido')